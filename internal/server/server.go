// replyRequested reports whether the publish asked for an inline reply box via
// ?reply=question (the Q&A/teaching mode) or a plain truthy ?reply. The box is a
// server-rendered, JS-free form that posts same-origin to the page's own URL.
func replyRequested(q url.Values) bool {
	if !q.Has("reply") {
		return false
	}
	switch strings.ToLower(strings.TrimSpace(q.Get("reply"))) {
	case "0", "false", "no", "off":
		return false
	default: // "question", "comment", "1", "true", "on", bare ?reply
		return true
	}
}

// replySlugFor returns the slug the rendered page's reply box should target, or
// "" to omit the box. handlePublish has already validated that a named slug is
// present whenever the box was requested.
func replySlugFor(enabled bool, named string) string {
	if enabled {
		return named
	}
	return ""
}

// renderedName returns a filename whose extension is .html (so the stored
// artifact is served as HTML), derived from the upload's name when possible.
func renderedName(filename string) string {
	switch {
	case filename == "":
		return "page.html"
	case strings.HasSuffix(filename, ".md"):
		return strings.TrimSuffix(filename, ".md") + ".html"
	case strings.HasSuffix(filename, ".markdown"):
		return strings.TrimSuffix(filename, ".markdown") + ".html"
	case strings.HasSuffix(filename, ".html"):
		return filename
	default:
		return filename + ".html"
	}
}

