// landingTableCap bounds the landing's "Published" preview: the newest few, with
// a link to /gallery for the rest. The full wall lives at /gallery; the landing
// only teases it (the report's "landing is two pages pretending to be one" fix).
const landingTableCap = 8

// landingRow is one row of the landing "Published" table after series collapse.
// A single-member family renders as its own slug; a family of two or more folds
// into one reference (label = the shared prefix, count = the size, slug = the
// newest member the row links to).
type landingRow struct {
	label   string    // display text: full slug (singleton) or prefix (series)
	slug    string    // link target: the row's own slug, or the newest member's
	count   int       // family size (1 for a singleton)
	created time.Time // newest member's publish time
	locked  bool      // lock glyph (singletons only; a series is mixed)
}

// collapseSeries folds a newest-first artifact slice into landing rows, grouping
// by slug prefix (slugGroup: text before the first hyphen). A family of two or
// more collapses to a single reference linking to its newest member; a lone
// artifact stays a plain row (no forced "(1)"). Row order follows first-sighting,
// which — because the input is newest-first — orders families by their newest
// member. This is the landing analogue of the gallery's client-side prefix
// grouping (demiplane-0pw).
func collapseSeries(pub []store.Artifact) []landingRow {
	type acc struct {
		idx     int // position in rows, to update in place
		members int
	}
	seen := make(map[string]*acc)
	var rows []landingRow
	for _, a := range pub {
		key := slugGroup(a.Slug)
		if g, ok := seen[key]; ok {
			// A second (or later) member promotes the row to a series: relabel to
			// the shared prefix, bump the count. The link/date stay on the newest
			// member (the first one seen), and a mixed family drops the lock glyph.
			rows[g.idx].label = key
			rows[g.idx].count = g.members + 1
			rows[g.idx].locked = false
			g.members++
			continue
		}
		rows = append(rows, landingRow{
			label:   a.Slug,
			slug:    a.Slug,
			count:   1,
			created: a.CreatedAt,
			locked:  a.HasPassword(),
		})
		seen[key] = &acc{idx: len(rows) - 1, members: 1}
	}
	return rows
}

// artifactTable renders the non-private artifact listing for the --browse case.
// Private artifacts are excluded (their capability slug is the secret). Links
// point at the CONTENT origin via contentBase (ADR 0003): the control-plane base
// has no GET /{slug}, so linking there returned 405. store.List is newest-first;
// a slug-series collapses to one row (collapseSeries) and the preview is capped
// at landingTableCap with an "all N" link to /gallery. The table shows the slug
// and the published date only — type/size clutter lives in the API, not here
// (demiplane-0pw).
func (s *Server) artifactTable(r *http.Request) string {
	arts, err := s.store.List(store.DefaultOwner)
	if err != nil {
		log.Printf("browse list failed: %v", err)
		return ""
	}
	base := s.contentBase(r)

	// Collect the public artifacts (newest-first order preserved from store.List).
	var pub []store.Artifact
	for _, a := range arts {
		if a.Private {
			continue
		}
		pub = append(pub, a)
	}

	var b strings.Builder
	b.WriteString(`<h2>Published</h2>`)
	if len(pub) == 0 {
		b.WriteString(`<p><em>No public artifacts yet. Publish one above.</em></p>`)
		return b.String()
	}

	rows := collapseSeries(pub)

	b.WriteString(`<table><thead>`)
	b.WriteString(`<tr><th>artifact</th><th>published</th></tr>`)
	b.WriteString(`</thead><tbody>`)
	shown := rows
	if len(shown) > landingTableCap {
		shown = shown[:landingTableCap]
	}
	for _, row := range shown {
		label := html.EscapeString(row.label)
		if row.count > 1 {
			// A series reads as "dispatch (8) →", linking to the newest member.
			label = fmt.Sprintf(`%s <span class="scount">(%d)</span> →`, label, row.count)
		} else if row.locked {
			label += " 🔒"
		}
		fmt.Fprintf(&b,
			`<tr><td><a href="%s/%s">%s</a></td><td>%s</td></tr>`,
			html.EscapeString(base), html.EscapeString(row.slug), label,
			row.created.Format("2006-01-02 15:04"))
	}
	b.WriteString(`</tbody></table>`)
	if len(rows) > landingTableCap {
		fmt.Fprintf(&b, `<p><a href="/gallery">All %s → /gallery</a></p>`, plural(len(pub), "artifact"))
	} else {
		b.WriteString(`<p><a href="/gallery">Browse the gallery →</a></p>`)
	}
	return b.String()
}

// plural formats a count with its noun, adding an "s" for anything but 1, so
// human copy reads "1 artifact" / "5 artifacts" instead of "artifact(s)".
func plural(n int, noun string) string {
	if n == 1 {
		return "1 " + noun
	}
	return strconv.Itoa(n) + " " + noun + "s"
}

// redactSlug returns a short, non-reversible tag for a slug so error logs never
// leak the slug itself — for a private artifact the slug IS the capability
// secret, and logs are a common exposure surface.
func redactSlug(slug string) string {
	sum := sha256.Sum256([]byte(slug))
	return "slug#" + hex.EncodeToString(sum[:])[:12]
}

// shortType trims charset/boundary params from a content type for display.
func shortType(ct string) string {
	if i := strings.IndexByte(ct, ';'); i >= 0 {
		return strings.TrimSpace(ct[:i])
	}
	return ct
}

// requestBase returns the advertised base URL (no trailing slash). With
// --base-url set it is authoritative; otherwise scheme+host are derived from
// the request. X-Forwarded-* are deliberately NOT consulted (host-header /
// proto spoofing defense), and the reflected host is sanitized: it flows into
// response bodies (llms.txt, publish responses, minted URLs), so a crafted
// Host header must not be able to inject arbitrary content there.
func (s *Server) requestBase(r *http.Request) string {
	if s.baseURL != "" {
		return s.baseURL
	}
	scheme := "http"
	if r.TLS != nil {
		scheme = "https"
	}
	return scheme + "://" + sanitizeHost(r.Host)
}

// sanitizeHost strips every character outside the URI host grammar (RFC 3986
// reg-name letters/digits/. /- /_ plus the :port separator and IPv6 brackets).
// Legitimate Host headers pass through untouched; a crafted one cannot smuggle
// spaces, quotes, angle brackets, or control characters into the plain-text and
// HTML surfaces that reflect the request host (Shannon finding demiplane-6y9).
func sanitizeHost(host string) string {
	return strings.Map(func(r rune) rune {
		switch {
		case r >= 'a' && r <= 'z', r >= 'A' && r <= 'Z', r >= '0' && r <= '9':
			return r
		case r == '.', r == '-', r == ':', r == '[', r == ']', r == '_':
			return r
		}
		return -1
	}, host)
}

// contentBase returns the advertised origin for artifact bodies (GET /{slug}).
// Artifacts live on a separate origin from the control plane (ADR 0003), so a
// URL minted while handling a control-listener request (/publish, /list) must
// point at the content origin, not the request's own host. Resolution order:
//  1. an explicit --content-base-url (proxy/TLS deployments);
//  2. derive from the request host, swapping in the content port (the zero-config
//     two-port default — handles loopback and wildcard binds alike);
//  3. same-origin mode (no content port): the request origin itself.
func (s *Server) contentBase(r *http.Request) string {
	if s.contentBaseURL != "" {
		return s.contentBaseURL
	}
	if s.contentPort == "" {
		return s.requestBase(r)
	}
	scheme := "http"
	if r.TLS != nil {
		scheme = "https"
	}
	host := sanitizeHost(r.Host)
	if h, _, err := net.SplitHostPort(host); err == nil {
		host = h
	}
	return scheme + "://" + net.JoinHostPort(host, s.contentPort)
}

// artifactURL builds the full URL for a slug on the content origin.
func (s *Server) artifactURL(r *http.Request, slug string) string {
	return s.contentBase(r) + "/" + slug
}

func wantsJSON(r *http.Request) bool {
	return strings.Contains(r.Header.Get("Accept"), "application/json")
}

// queryFlag reports whether a boolean query parameter is set true. A bare
// "?private" (present, empty value) counts as true; "?private=false" (and 0/no/
// off) counts as false; an absent key is false.
func queryFlag(q url.Values, key string) bool {
	if !q.Has(key) {
		return false
	}
	switch strings.ToLower(strings.TrimSpace(q.Get(key))) {
	case "0", "false", "no", "off":
		return false
	default:
		return true
	}
}

// passwordOK reports whether the request carries HTTP Basic credentials whose
// password matches the artifact's hash. The username is ignored.
func passwordOK(r *http.Request, hash string) bool {
	_, pass, ok := r.BasicAuth()
	if !ok {
		return false
	}
	return store.PasswordMatches(hash, pass)
}

// rfc3339OrEmpty formats t as RFC3339, or "" for the zero time.
func rfc3339OrEmpty(t time.Time) string {
	if t.IsZero() {
		return ""
	}
	return t.Format(time.RFC3339)
}

func writeJSON(w http.ResponseWriter, status int, v any) {
	w.Header().Set("Content-Type", "application/json; charset=utf-8")
	w.WriteHeader(status)
	_ = json.NewEncoder(w).Encode(v)
}

// isClientError reports whether err is a user-input problem (bad slug, invalid
// option combination) that should be a 400 rather than a 500. It matches the
// typed store.InputError, not error text — robust to message changes and to
// internal errors that happen to mention "slug".
func isClientError(err error) bool {
	var ie *store.InputError
	return errors.As(err, &ie)
}

// sanitizeFilename strips characters that would break the Content-Disposition
// header. Quotes and control characters are removed; the rest passes through.
func sanitizeFilename(name string) string {
	name = strings.Map(func(r rune) rune {
		if r < 0x20 || r == '"' || r == '\\' || r == '/' {
			return -1
		}
		return r
	}, name)
	if name == "" {
		return "download"
	}
	return name
}
