// Package render provides demiplane's optional, dependency-free markdown→HTML
// rendering. It implements a deliberate *subset* of CommonMark — headings,
// fenced code, lists, blockquotes, horizontal rules, paragraphs, and inline
// code/bold/italic/links — which covers the report-style documents demiplane is
// built to host. It is NOT a complete CommonMark implementation; if richer
// rendering is ever needed, a vetted library can be swapped in behind this same
// Markdown function. All source text is HTML-escaped before inline formatting,
// so raw HTML/JS in the markdown source cannot inject into the rendered page.
package render

import (
	"html"
	"regexp"
	"strconv"
	"strings"
	"time"

	"github.com/daisandapex/demiplane/internal/theme"
)

// Options controls how Markdown wraps rendered content in a styled document.
// The zero value renders the default house style (theme.Default) with no
// masthead or footer (callers opt into the chrome explicitly).
type Options struct {
	// Theme selects a built-in theme by name (see internal/theme: "light" /
	// "dark"). Empty means "no explicit server theme": the page renders light
	// server-side, and the client toggle's initial state falls back to the OS
	// prefers-color-scheme. A non-empty value is the initial/default theme.
	Theme string
	// CSS, when non-empty, is a complete stylesheet that REPLACES the built-in
	// theme — the --css self-host-branding override. Trusted operator input
	// (a file path the operator chose), injected verbatim into <style>. When
	// set, the client light/dark toggle is disabled (the custom sheet owns the
	// look and need not define both token sets).
	CSS string
	// Title is the fallback document title for the masthead, used when the body
	// has no level-1 heading. Typically the artifact slug.
	Title string
	// Header renders the sticky masthead (document title + theme toggle). When
	// set, the body's first H1 is lifted into the masthead (not duplicated).
	Header bool
	// Footer renders the "Generated by demiplane" vanity footer.
	Footer bool
	// FooterLink is the footer link target; empty renders the wordmark unlinked.
	FooterLink string
	// MetaHeader controls YAML-frontmatter handling. When true, a leading
	// `---`-fenced `key: value` block is lifted out of the body and rendered as a
	// styled meta-header (a localized date/published tstamp plus one labeled line
	// per remaining field) below the masthead title. When false, any frontmatter
	// is stripped and ignored (no header). With no frontmatter, behavior is
	// unchanged either way.
	MetaHeader bool
	// ReplySlug, when non-empty, bakes a first-class inline reply box at the foot
	// of the page (the ?reply=question publish mode). It is the artifact's own
	// (named) slug: the box's JS-free <form> posts to /answer/<slug> — an
	// absolute-path action, so it lands SAME-ORIGIN on the content plane that
	// served the page (scheme inherited → correct once TLS lands). The content
	// plane's submit handler renders a server-truth confirmation, so false success
	// is structurally impossible. A single free-text answer is recorded as a
	// comment-kind reply, fitting Q&A/teaching use. It must be a URL-safe slug (it
	// always is by construction) and is attribute-escaped defensively.
	ReplySlug string
	// Colophon, when true, appends the editorial colophon at the foot of the
	// document (demiplane-rwj items 1+2): an optional series prev/next nav row and
	// a meta row (published date · size · link to the instance gallery). Enabled
	// for the ?render=md publish path; the metadata below feeds it.
	Colophon bool
	// Slug is the artifact's own slug — the series family key and the anchor the
	// prev/next order around. Distinct from Title, which the body's first H1 may
	// override; the series computation must key off the stable slug, not the title.
	Slug string
	// Published is the artifact's publish timestamp, shown (date only) in the
	// colophon meta row. It matches the stored CreatedAt (set moments later at Put).
	Published time.Time
	// Siblings lists the slugs of already-published artifacts in the same slug
	// family (shared prefix before the first hyphen), from which series prev/next is
	// computed. It excludes the current artifact, which is not yet stored at render
	// time. A family of fewer than two total members renders no nav row (a singleton
	// shows the meta row alone).
	Siblings []string
	// ContentBase is the origin the series prev/next links are minted against (the
	// content plane that serves artifact bodies, ADR 0003). Empty renders the nav
	// slugs unlinked.
	ContentBase string
	// IndexURL is the absolute link for the colophon's "all artifacts →" — the
	// instance gallery on the control plane, honoring the base-URL convention so it
	// stays instance-portable. Empty omits the link.
	IndexURL string
	// ReplyNext, when non-empty (only meaningful with ReplySlug), names the
	// FOLLOW-UP slug the student flows to after answering — the ?next= publish
	// param. It travels as a hidden form field; after a recorded answer the
	// confirmation page waits for this slug to be published and then carries the
	// student there. Validated at publish (slug-shaped, ≠ ReplySlug) and
	// re-validated at submit (a hidden field is client-tamperable).
	ReplyNext string
}

var (
	reHeading = regexp.MustCompile(`^(#{1,6})\s+(.*)$`)
	reOrdered = regexp.MustCompile(`^\d+\.\s+(.*)$`)
	reHRule   = regexp.MustCompile(`^(-{3,}|\*{3,}|_{3,})$`)
	// reTableSep matches a GFM table separator row, e.g. |---|:--:|--:| (with
	// optional alignment colons), so a preceding pipe row is treated as a header.
	reTableSep = regexp.MustCompile(`^\s*\|?\s*:?-+:?\s*(\|\s*:?-+:?\s*)*\|?\s*$`)

	// reTag matches an HTML tag, for reducing inline-rendered title HTML to the
	// plain text an element <title> needs.
	reTag = regexp.MustCompile(`<[^>]*>`)

	// Inline patterns, applied to already-escaped text.
	reCode    = regexp.MustCompile("`([^`]+)`")
	reLink    = regexp.MustCompile(`\[([^\]]+)\]\(([^)\s]+)\)`)
	reBold    = regexp.MustCompile(`\*\*([^*]+)\*\*`)
	reItalic  = regexp.MustCompile(`\*([^*]+)\*`)
	reItalicU = regexp.MustCompile(`_([^_]+)_`)
)

// Markdown renders src as a complete, styled HTML document (used for the
// ?render=md publish path). opts selects the theme/custom stylesheet and the
// optional masthead + footer chrome; the design tokens and typography are the
// same the /docs pages use, shared via internal/theme.
func Markdown(src []byte, opts Options) []byte {
	body := src

	// Frontmatter: a leading `---`-fenced key:value block. When the meta-header is
	// on we render it below the title; when off (or always, if it's there) we
	// strip it so the raw fence never leaks into the body. No frontmatter → no-op.
	var metaHTML string
	var metaNeedsScript bool
	if fields, rest, ok := parseFrontmatter(body); ok {
		body = rest
		if opts.MetaHeader {
			metaHTML, metaNeedsScript = metaHeader(fields)
		}
	}

	// The masthead shows the document title; lifting the first H1 into it avoids
	// a duplicate giant title in the body. Without a header, the H1 stays put.
	title := html.EscapeString(opts.Title)
	if opts.Header {
		if h1, rest := splitFirstH1(body); h1 != "" {
			title, body = h1, rest
		}
	}
	if title == "" {
		title = "Untitled"
	}
	// titleText is the plain-text form for the <head><title>: the masthead title
	// may carry inline markup (a lifted H1 rendered <code>/<em>/<a>), which is
	// inert in an element title, so strip tags. Entities stay escaped.
	titleText := stripTags(title)

	// Stylesheet: a --css override owns the look (toggle off); a pinned named
	// theme (catppuccin/dracula/one-dark) fixes its single palette (toggle off,
	// nothing to flip to); otherwise the masthead ships both token sets so the
	// client light/dark toggle can flip instantly.
	toggle := opts.CSS == "" && opts.Header && !theme.Pinned(opts.Theme)
	var css string
	switch {
	case opts.CSS != "":
		css = opts.CSS
	case toggle:
		css = theme.ToggleCSS()
	default:
		css = theme.CSS(opts.Theme)
	}
	if opts.Header || opts.Footer {
		css += docChromeCSS
	}
	if metaHTML != "" {
		css += metaHeadCSS
	}
	if opts.ReplySlug != "" {
		css += replyBoxCSS
	}
	if opts.Colophon {
		css += coloCSS
	}
	// The print sheet forces a page-friendly light palette and unsticks the
	// chrome regardless of the on-screen theme. Skip it only when the operator
	// supplied a full --css override (their sheet owns print too).
	if opts.CSS == "" {
		css += printCSS
	}

	var b strings.Builder
	b.WriteString(docHead(css, titleText, opts.Theme, toggle))
	if opts.Header {
		b.WriteString(masthead(title, opts.Theme, toggle))
	}
	b.WriteString("<main class=\"wrap\">\n")
	b.WriteString(metaHTML)
	bodyHTML := Body(body)
	if opts.Header {
		// The first body paragraph is the document's dek. Tag it explicitly so the
		// lead treatment targets .lead, not a positional selector that breaks the
		// moment a frontmatter meta-header is injected ahead of the body.
		bodyHTML = tagLead(bodyHTML)
	}
	b.WriteString(bodyHTML)
	if opts.ReplySlug != "" {
		b.WriteString(replyBox(opts.ReplySlug, opts.ReplyNext))
	}
	if opts.Colophon {
		b.WriteString(colophon(opts))
	}
	b.WriteString("</main>\n")
	if opts.Footer {
		b.WriteString(footerHTML(opts.FooterLink))
	}
	if opts.Header {
		b.WriteString(docChromeScript)
	}
	if metaNeedsScript {
		b.WriteString(tstampScript)
	}
	b.WriteString(docTail)
	// The colophon's size sentinel is now resolved against the finished document
	// length — the exact byte count the store will record for this artifact.
	return []byte(fillSize(b.String()))
}

// splitFirstH1 returns the inline-rendered text of the document's first level-1
// heading and src with that heading line removed, so the title can move into the
// masthead without being duplicated in the body. Headings inside fenced code are
// ignored. If there is no H1, it returns "" and the original src unchanged.
func splitFirstH1(src []byte) (titleHTML string, rest []byte) {
	lines := strings.Split(strings.ReplaceAll(string(src), "\r\n", "\n"), "\n")
	inFence := false
	for i, ln := range lines {
		t := strings.TrimSpace(ln)
		if strings.HasPrefix(t, "```") {
			inFence = !inFence
			continue
		}
		if inFence {
			continue
		}
		if m := reHeading.FindStringSubmatch(t); m != nil && len(m[1]) == 1 {
			kept := append(append([]string{}, lines[:i]...), lines[i+1:]...)
			return inline(m[2]), []byte(strings.Join(kept, "\n"))
		}
	}
	return "", src
}

// masthead renders the sticky title bar: a kicker (the wordmark, small-caps and
// accented), the document title set as the page's single display-serif H1, and,
// unless a custom stylesheet is in force, the light/dark toggle. The toggle
// carries both a sun and a moon SVG; CSS shows the one matching data-theme.
//
// serverTheme is the resolved initial theme ("" | light | dark); the toggle's
// aria-pressed reports "dark is active" for assistive tech. It is a server best
// guess (the client init script may resolve a different theme from localStorage /
// prefers-color-scheme); docChromeScript re-syncs aria-pressed on load and flips
// it on click, so the exposed state always matches the actual data-theme.
func masthead(titleHTML, serverTheme string, toggle bool) string {
	var b strings.Builder
	b.WriteString(`<header class="docbar"><div class="wrap"><div class="masthead">`)
	b.WriteString(`<span class="kicker">demiplane</span>`)
	b.WriteString(`<h1 class="doctitle">` + titleHTML + `</h1>`)
	b.WriteString(`</div>`)
	if toggle {
		pressed := "false"
		if serverTheme == "dark" {
			pressed = "true"
		}
		b.WriteString(`<button class="themetoggle" type="button" aria-pressed="` + pressed + `" ` +
			`aria-label="Toggle light or dark theme" title="Toggle theme">` +
			sunSVG + moonSVG + `</button>`)
	}
	b.WriteString("</div></header>\n")
	return b.String()
}

// stripTags reduces inline-rendered HTML to its text content by removing tags.
// Used to derive the element <title> from a masthead title that may carry inline
// markup; HTML entities in the text are left escaped (valid title content).
func stripTags(s string) string { return reTag.ReplaceAllString(s, "") }

// tagLead marks the first paragraph of rendered body HTML as the lead/dek by
// adding class="lead", but only when the document actually leads with a
// paragraph (not a heading, list, or table). The renderer knows the block
// structure, so this replaces the fragile positional CSS selector.
func tagLead(bodyHTML string) string {
	if strings.HasPrefix(bodyHTML, "<p>") {
		return strings.Replace(bodyHTML, "<p>", `<p class="lead">`, 1)
	}
	return bodyHTML
}

// headingText reduces a heading's raw markdown to the plain text used for its
// slug id and permalink aria-label: it runs the same inline pipeline the body
// uses (so [text](url) collapses to "text", `code` to "code", **bold** to
// "bold"), strips the resulting tags, and unescapes entities back to literal
// text. slugify then keeps only [a-z0-9-], and the aria-label re-escapes for the
// attribute, so no unescaped source can reach either sink.
func headingText(raw string) string {
	return html.UnescapeString(stripTags(inline(raw)))
}

// slugify builds a URL-safe fragment id from heading text: lowercased, runs of
// whitespace or existing hyphens collapse to a single hyphen, every other
// character (punctuation, entities, non-ASCII) is dropped, and leading/trailing
// hyphens are trimmed. The output is drawn from [a-z0-9-] only, so it is safe to
// interpolate into both an id attribute and an href fragment without escaping.
func slugify(s string) string {
	var b strings.Builder
	prevHyphen := false
	for _, r := range strings.ToLower(s) {
		switch {
		case (r >= 'a' && r <= 'z') || (r >= '0' && r <= '9'):
			b.WriteRune(r)
			prevHyphen = false
		case r == ' ' || r == '\t' || r == '-' || r == '_':
			if b.Len() > 0 && !prevHyphen {
				b.WriteByte('-')
				prevHyphen = true
			}
		}
	}
	return strings.TrimRight(b.String(), "-")
}

// uniqueSlug returns slugify(text) made unique within one document by appending
// a numeric suffix (-1, -2, …) on collision, recording every id it hands out
// (including the suffixed forms) so a later natural "foo-1" heading cannot
// duplicate a generated one. An empty slug (a heading of only punctuation or
// emoji) falls back to "section". Deterministic for a given heading sequence.
func uniqueSlug(text string, used map[string]bool) string {
	base := slugify(text)
	if base == "" {
		base = "section"
	}
	s := base
	for i := 1; used[s]; i++ {
		s = base + "-" + strconv.Itoa(i)
	}
	used[s] = true
	return s
}

// headingAnchor renders the quiet hover-'#' permalink placed at the end of every
// rendered heading. It links to the heading's own fragment and carries an
// aria-label ("Permalink to <heading>") for assistive tech. id is slug-safe by
// construction; plain is attribute-escaped. The affordance is hidden until the
// heading is hovered or the link is focused (see contentCSS .heading-anchor), and
// reserves its own space so revealing it shifts no layout.
func headingAnchor(id, plain string) string {
	return `<a class="heading-anchor" href="#` + id +
		`" aria-label="Permalink to ` + html.EscapeString(plain) + `">#</a>`
}

