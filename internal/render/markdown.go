// Body renders src to inner HTML only (no <html>/<head> wrapper), so callers
// such as the /docs route can place it inside their own page chrome while
// reusing this single markdown engine.
func Body(src []byte) string {
	return renderBlocks(string(src))
}

// renderBlocks converts markdown block structure to HTML.
func renderBlocks(src string) string {
	lines := strings.Split(strings.ReplaceAll(src, "\r\n", "\n"), "\n")
	var out strings.Builder
	var para []string
	// used records the heading ids emitted so far in this document so colliding
	// slugs get a deterministic numeric suffix (uniqueSlug). Scoped per render, so
	// ids are stable for a given source and independent across documents.
	used := map[string]bool{}

	flushPara := func() {
		if len(para) == 0 {
			return
		}
		out.WriteString("<p>")
		out.WriteString(inline(strings.Join(para, " ")))
		out.WriteString("</p>\n")
		para = para[:0]
	}

	for i := 0; i < len(lines); i++ {
		line := lines[i]
		trimmed := strings.TrimSpace(line)

		switch {
		case strings.HasPrefix(trimmed, "```"):
			flushPara()
			// The fence info-string names the language (```go); its first token
			// selects the syntax highlighter (highlight.go). Collect until close.
			lang := strings.TrimSpace(trimmed[3:])
			var code []string
			for i++; i < len(lines) && !strings.HasPrefix(strings.TrimSpace(lines[i]), "```"); i++ {
				code = append(code, lines[i])
			}
			joined := strings.Join(code, "\n")
			out.WriteString("<pre><code")
			if canon := canonLang(lang); canon != "" {
				out.WriteString(` class="language-` + canon + `"`)
			}
			out.WriteString(">")
			// highlight() escapes every byte it emits (classed span or plain run);
			// an unsupported language returns the source HTML-escaped and unstyled,
			// so the raw-HTML-in-code XSS guard holds either way.
			out.WriteString(highlight(lang, joined))
			out.WriteString("</code></pre>\n")

		case trimmed == "":
			flushPara()

		case reHRule.MatchString(trimmed):
			flushPara()
			out.WriteString("<hr>\n")

		case reHeading.MatchString(trimmed):
			flushPara()
			m := reHeading.FindStringSubmatch(trimmed)
			level := len(m[1])
			plain := headingText(m[2])
			id := uniqueSlug(plain, used)
			lvl := byte('0' + level)
			out.WriteString("<h")
			out.WriteByte(lvl)
			out.WriteString(` id="` + id + `">`)
			out.WriteString(inline(m[2]))
			out.WriteString(headingAnchor(id, plain))
			out.WriteString("</h")
			out.WriteByte(lvl)
			out.WriteString(">\n")

		case strings.HasPrefix(trimmed, "> "):
			flushPara()
			var quote []string
			for i < len(lines) && strings.HasPrefix(strings.TrimSpace(lines[i]), "> ") {
				quote = append(quote, strings.TrimPrefix(strings.TrimSpace(lines[i]), "> "))
				i++
			}
			i--
			out.WriteString("<blockquote>")
			out.WriteString(inline(strings.Join(quote, " ")))
			out.WriteString("</blockquote>\n")

		case isUnordered(trimmed) || reOrdered.MatchString(trimmed):
			flushPara()
			listHTML, next := renderList(lines, i)
			out.WriteString(listHTML)
			i = next - 1

		case strings.Contains(trimmed, "|") && i+1 < len(lines) && reTableSep.MatchString(lines[i+1]):
			// GFM pipe table: header row, |---| separator, then body rows until a
			// blank or non-pipe line.
			flushPara()
			header := splitCells(trimmed)
			i += 2 // consume header + separator; i now at the first body row
			var rows [][]string
			for i < len(lines) {
				lt := strings.TrimSpace(lines[i])
				if lt == "" || !strings.Contains(lt, "|") {
					break
				}
				rows = append(rows, splitCells(lt))
				i++
			}
			i-- // the outer loop's i++ advances past the last consumed row
			out.WriteString(renderTable(header, rows))

		default:
			para = append(para, trimmed)
		}
	}
	flushPara()
	return out.String()
}

func isUnordered(s string) bool {
	return strings.HasPrefix(s, "- ") || strings.HasPrefix(s, "* ") || strings.HasPrefix(s, "+ ")
}

// renderList consumes a contiguous run of list items starting at lines[start]
// and renders it to nested <ul>/<ol> HTML, returning the index of the first line
// past the list. A list run is the maximal block of consecutive non-blank lines
// whose trimmed form is an ordered (`1. `) or unordered (`- `/`* `/`+ `) item; a
// blank or non-item line ends it (matching the flat behavior it replaces).
//
// Nesting is driven by leading indentation (the house convention is two spaces
// per level, but any consistent widening nests and any narrowing closes, so
// deeper or ragged indentation still yields a valid tree). Ordered and unordered
// items mix freely: a change of marker kind at the same indent closes the current
// list and opens the other. Item text runs through inline(), which HTML-escapes
// before formatting, so the escape-first XSS posture is unchanged.
//
// Invariant: each open list frame always has exactly one <li> whose </li> is
// deferred until a sibling replaces it, a child list closes above it, or the run
// unwinds — which is what lets a nested list live inside its parent's still-open
// <li>.
func renderList(lines []string, start int) (string, int) {
	type frame struct {
		indent  int
		ordered bool
	}
	var b strings.Builder
	var stack []frame

	openTag := func(ordered bool) {
		if ordered {
			b.WriteString("<ol>\n")
		} else {
			b.WriteString("<ul>\n")
		}
	}
	closeTag := func(ordered bool) {
		if ordered {
			b.WriteString("</ol>\n")
		} else {
			b.WriteString("</ul>\n")
		}
	}

	i := start
	for i < len(lines) {
		raw := lines[i]
		trimmed := strings.TrimSpace(raw)
		ordered := reOrdered.MatchString(trimmed)
		unordered := isUnordered(trimmed)
		if !ordered && !unordered {
			break
		}
		indent := len(raw) - len(strings.TrimLeft(raw, " \t"))
		var content string
		if ordered {
			content = reOrdered.FindStringSubmatch(trimmed)[1]
		} else {
			content = trimmed[2:]
		}

		// Ascend: close every level deeper than this item's indent. Each pop closes
		// that level's open <li> and its list tag; the parent <li> it lived inside is
		// closed by the next shallower iteration (or by the sibling/unwind below).
		for len(stack) > 0 && indent < stack[len(stack)-1].indent {
			b.WriteString("</li>\n")
			closeTag(stack[len(stack)-1].ordered)
			stack = stack[:len(stack)-1]
		}

		if len(stack) > 0 && indent == stack[len(stack)-1].indent {
			// Sibling at the same level: close the previous item, then reopen as the
			// other marker kind if this item switched ordered<->unordered.
			b.WriteString("</li>\n")
			if stack[len(stack)-1].ordered != ordered {
				closeTag(stack[len(stack)-1].ordered)
				stack = stack[:len(stack)-1]
				openTag(ordered)
				stack = append(stack, frame{indent: indent, ordered: ordered})
			}
		} else {
			// Deeper than the current top (or the very first item): open a new list.
			// When deeper, it nests inside the parent's still-open <li>.
			openTag(ordered)
			stack = append(stack, frame{indent: indent, ordered: ordered})
		}

		b.WriteString("<li>")
		b.WriteString(inline(content))
		i++
	}

	// Unwind any levels left open at the end of the run.
	for len(stack) > 0 {
		b.WriteString("</li>\n")
		closeTag(stack[len(stack)-1].ordered)
		stack = stack[:len(stack)-1]
	}
	return b.String(), i
}

// splitCells splits a GFM table row into inline-rendered cell HTML. Leading and
// trailing pipes are stripped; each cell goes through inline(), which escapes
// before formatting — so cell content (including raw HTML) cannot inject.
func splitCells(row string) []string {
	s := strings.TrimSpace(row)
	s = strings.TrimPrefix(s, "|")
	s = strings.TrimSuffix(s, "|")
	parts := strings.Split(s, "|")
	cells := make([]string, len(parts))
	for i, p := range parts {
		cells[i] = inline(strings.TrimSpace(p))
	}
	return cells
}

// renderTable emits a <table> from a header and body rows of already-escaped,
// inline-rendered cell HTML. Rows are padded/truncated to the header width. The
// table is wrapped in a .table-wrap container that scrolls horizontally on its
// own (overflow-x:auto), so a table wider than the text measure scrolls inside
// its box instead of forcing the whole document body to scroll sideways on
// mobile.
func renderTable(header []string, rows [][]string) string {
	var b strings.Builder
	b.WriteString(`<div class="table-wrap">` + "\n")
	b.WriteString("<table>\n<thead><tr>")
	for _, h := range header {
		b.WriteString("<th>" + h + "</th>")
	}
	b.WriteString("</tr></thead>\n<tbody>\n")
	for _, r := range rows {
		b.WriteString("<tr>")
		for c := range header {
			cell := ""
			if c < len(r) {
				cell = r[c]
			}
			b.WriteString("<td>" + cell + "</td>")
		}
		b.WriteString("</tr>\n")
	}
	b.WriteString("</tbody>\n</table>\n")
	b.WriteString("</div>\n")
	return b.String()
}

// inline escapes text and applies inline formatting. Escaping happens first so
// raw HTML in the source is rendered as text, never executed.
func inline(s string) string {
	s = html.EscapeString(s)
	s = reCode.ReplaceAllString(s, "<code>$1</code>")
	s = reLink.ReplaceAllStringFunc(s, renderLink)
	s = reBold.ReplaceAllString(s, "<strong>$1</strong>")
	s = reItalic.ReplaceAllString(s, "<em>$1</em>")
	s = reItalicU.ReplaceAllString(s, "<em>$1</em>")
	return s
}

// renderLink turns [text](url) into an anchor. Two layers of XSS defense:
//
//  1. Both text and url reach here ALREADY HTML-escaped, because inline()
//     escapes the whole line before any inline regex runs. So a url like
//     `https://a"onmouseover="x` arrives as `https://a&#34;onmouseover=&#34;x`
//     and cannot break out of the href attribute; injected text is inert too.
//  2. The URL scheme is checked against an ALLOWLIST (below); anything that is
//     not an explicitly-permitted scheme or a relative reference is dropped to
//     plain text, so javascript:/data:/vbscript: and unknown schemes never
//     become a live href. Rendered markdown is served same-origin, so this
//     matters.
func renderLink(match string) string {
	m := reLink.FindStringSubmatch(match)
	text, u := m[1], m[2]
	if !safeURL(u) {
		return text
	}
	return `<a href="` + u + `" rel="noopener noreferrer">` + text + `</a>`
}

// safeURL is an allowlist: it permits http/https/mailto absolute URLs and
// relative references (path, fragment, query), and rejects everything else —
// notably any other scheme. The input is already HTML-escaped; scheme tokens
// (http, https, mailto) contain no escapable characters, so prefix checks are
// reliable.
func safeURL(u string) bool {
	lu := strings.ToLower(strings.TrimSpace(u))
	if lu == "" {
		return false
	}
	if strings.HasPrefix(lu, "http://") || strings.HasPrefix(lu, "https://") || strings.HasPrefix(lu, "mailto:") {
		return true
	}
	// Protocol-relative URLs ("//host/path") are external navigations, not
	// same-origin relative refs — reject them so a published markdown link cannot
	// silently redirect off-site (open-redirect / phishing). Must precede the
	// single-"/" relative allow below, which would otherwise match "//".
	if strings.HasPrefix(lu, "//") {
		return false
	}
	// Relative references are safe: they cannot carry an active-content scheme.
	if strings.HasPrefix(lu, "/") || strings.HasPrefix(lu, "#") || strings.HasPrefix(lu, "?") {
		return true
	}
	// Bare relative path (e.g. "page.html", "sub/page"): allow only if there is
	// no scheme-like colon before the first path separator.
	if colon := strings.IndexByte(lu, ':'); colon >= 0 {
		sep := strings.IndexAny(lu, "/#?")
		if sep == -1 || colon < sep {
			return false // looks like "scheme:..." → reject
		}
	}
	return true
}

// docHead returns the document head with css inlined and the initial data-theme
// set on <html>. When toggle is true a tiny inline script runs before the body
// paints, resolving the viewer's theme (localStorage → server default →
// prefers-color-scheme) with no flash of the wrong theme.
func docHead(css, titleText, themeName string, toggle bool) string {
	initial := themeName
	if initial == "" {
		initial = theme.Default
	}
	if titleText == "" {
		titleText = "Untitled"
	}
	head := `<!DOCTYPE html>
<html lang="en" data-theme="` + initial + `">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<meta name="theme-color" content="` + theme.MetaColor(initial) + `">
<title>` + titleText + `</title>
<style>` + css + `</style>
`
	if toggle {
		head += initThemeScript(themeName) + "\n"
	}
	return head + `</head>
<body>
`
}

// initThemeScript runs synchronously in <head> to set data-theme before paint.
// serverTheme is "" / "light" / "dark" (validated upstream), so embedding it in
// the JS string literal is injection-safe. Empty serverTheme means "no server
// default" → fall through to prefers-color-scheme.
func initThemeScript(serverTheme string) string {
	return `<script>(function(){try{var k='demiplane-theme',s=localStorage.getItem(k),d='` +
		serverTheme + `';var t=s||d||((window.matchMedia&&` +
		`matchMedia('(prefers-color-scheme: dark)').matches)?'dark':'light');` +
		`document.documentElement.setAttribute('data-theme',t);}catch(e){}})();</script>`
}

