// footerHTML renders the thin vanity footer, aligned to the text column. The
// link is operator-controlled (config, trusted) but still attribute-escaped. An
// empty link renders the wordmark unlinked.
func footerHTML(link string) string {
	mark := `<span class="mark">demiplane</span>`
	if link != "" {
		mark = `<a href="` + html.EscapeString(link) + `" rel="noopener noreferrer" class="mark">demiplane</a>`
	}
	return `<footer class="docfoot"><div class="wrap">Generated by ` + mark + "</div></footer>\n"
}

// replyBox renders the first-class inline reply box baked at the foot of a
// ?reply=question page, targeting slug's same-origin submit endpoint. Design
// invariants that make false success impossible:
//
//   - Absolute-path action (/answer/<slug>): the form posts to the content origin
//     that served the page — same-origin by construction, no cross-origin/mixed-
//     content failure, and correct once TLS lands (the scheme is inherited, not
//     hard-coded). The slug is attribute-escaped.
//   - No JavaScript: the browser navigates to the server's real response. The
//     "recorded" confirmation is server-rendered ONLY after the answer persists;
//     a failed write renders an honest error, never a success. Contrast the
//     dogfood incident's onsubmit timer that claimed success unconditionally.
//   - required textarea: a courtesy client-side guard; the server re-validates a
//     non-empty answer regardless.
//   - next (the ?next= publish param) rides along as a hidden field so the
//     server-rendered confirmation can flow the student to the follow-up lesson.
//     Hidden fields are client-tamperable, so the submit handler re-validates it;
//     it only ever produces a same-origin, slug-shaped link.
func replyBox(slug, next string) string {
	action := "/answer/" + html.EscapeString(slug)
	var nextField string
	if next != "" {
		nextField = `<input type="hidden" name="next" value="` + html.EscapeString(next) + `">` + "\n"
	}
	return `<section class="dp-reply" aria-labelledby="dp-reply-h">
<h2 id="dp-reply-h">Your answer</h2>
<form method="post" action="` + action + `">
` + nextField + `<textarea name="body" required placeholder="Your answer in your own words…"></textarea>
<div class="dp-reply-row"><button type="submit">Submit my answer</button></div>
</form>
<p class="dp-reply-note">Your answer posts to this server and is recorded before you see any confirmation.</p>
</section>
`
}

